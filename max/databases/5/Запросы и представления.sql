-- 5. Охранники библиотеки

CREATE VIEW Охранники_библиотеки
AS
SELECT *
FROM Сотрудник
WHERE Должность = 'охранник'
GO

SELECT * FROM Охранники_библиотеки

-- 6. Поздно возвращенные нотные издания

CREATE VIEW Поздно_возвращенные_нотные_издания
AS
SELECT Заглавие, Дата_реального_возврата
FROM Нотное_издание
JOIN Выдача_нотного_издания
ON dbo.Нотное_издание.ID = dbo.Выдача_нотного_издания.Нотное_издание
WHERE Дата_реального_возврата > Дата_предполагаемого_возврата
GO

SELECT * FROM Поздно_возвращенные_нотные_издания
ORDER BY Дата_реального_возврата

-- 7. Поступившие в текущем году диски

CREATE VIEW Поступившие_в_этом_году_диски
AS
SELECT *
FROM CD_диск
WHERE DATEPART(YY, Дата_поступления_в_библиотеку) = DATEPART(YY, GETDATE())
GO

SELECT * FROM Поступившие_в_этом_году_диски
ORDER BY Дата_поступления_в_библиотеку ASC

