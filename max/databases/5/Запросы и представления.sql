-- 1. Все CD-диски тиражом свыше 1000 экземпляров.

CREATE VIEW CD_диски_тиражом_свыше_1000_экземпляров
AS
SELECT *
FROM CD_диск
WHERE Тираж > 1000
GO

SELECT * FROM CD_диски_тиражом_свыше_1000_экземпляров

-- 2. Стенофонические грампластинки.

CREATE VIEW Стенофонические_грампластинки
AS
SELECT *
FROM Грампластинка
WHERE Формат_записи = 'стенофоническая'
GO

SELECT * FROM Стенофонические_грампластинки

-- 3. Невозвращенные нотные издания.

CREATE VIEW Невозвращенные_нотные_издания
AS
SELECT Заглавие, Дата_выдачи
FROM dbo.Выдача_нотного_издания
JOIN dbo.Нотное_издание
ON dbo.Выдача_нотного_издания.Нотное_издание = dbo.Нотное_издание.ID
GROUP BY Заглавие, Дата_выдачи
GO

SELECT * FROM Невозвращенные_нотные_издания

